package demos;

public class demo7 {
    public static void main(String[] args) {
        int inta = 50;
        int intb = 60;
        double doubc = 70.0;
        System.out.println(inta + intb + doubc);
        System.out.println(50 == inta);
        System.out.println(60 == (double)intb);
        System.out.println(60 == intb);
        System.out.println(70 == doubc);
        System.out.println(70.0 == doubc);
        System.out.println(70.0 == (long)doubc);

    }
}
